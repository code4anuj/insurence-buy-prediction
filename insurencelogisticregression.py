# Logistic Regression based Insurence Buying Prediction model
import pandas as pd
import numpy as np 
import seaborn as sns
import matplotlib.pyplot as plt
# %matplotlib inline
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression

data = pd.read_csv('/content/insurence_dataset.csv')
data.head(10)

plt.scatter(data.age,data.bought_insurence,marker='+',color='red')

X_train,X_test,y_train,y_test = train_test_split(data[['age']],data.bought_insurence,train_size=0.8)

regr = LogisticRegression()

regr.fit(X_train,y_train)

prd = regr.predict(X_test)
prd

print(regr.predict_proba(X_test))

